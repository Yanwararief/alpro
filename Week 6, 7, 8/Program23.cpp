#include <iostream>
// I.S Latihan VII (Program Pengulangan (Do))
// F.S Menampilkan proses pengulangan metode Do

using namespace std;

int main (){
    //Kamus
int a = 10;
    
    // Fungsi Pengulangan Do
    // Do { aksi; Increment; } while (Kondisi);
do {

    // Aksi
cout << "Nilai a: " << a << endl;

    //Increment
    a++;
}
    
    //While (Kondisi)
while(a < 20);  
    
    return 0;

}



