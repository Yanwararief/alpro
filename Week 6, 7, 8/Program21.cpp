#include <iostream>

// I.S Latihan V (Program Pengulangan (For))
// F.S Menampilkan proses pengulangan metode for

using namespace std;
int main (){
    //Kamus
int a;
    
    //Fungsi Pengulangan For..
    //Jenis data<spasi>nama variabel data = (Awal Pengulangan)/Inisialisasi ; 
    //nama variabel data <spasi> Kondisi <spasi> (Akhir Pengulangan); Increment (contoh : a = a - 1 (a--))
for(int a = 20 ; a > 10; a--) {

    //Menampilkan Pengulangan
cout << "Helloo " << a << endl;
}

    return 0;
}